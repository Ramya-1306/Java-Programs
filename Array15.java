public class Array15{
    public static void main(String args[]){
        int a[]={1,2,3,4,5};
        int b=3;
        int k=a.length-b;
        for(int i=b-1;i<a.length;i++){
            System.out.print(a[i]);
        }
        for(int i=0;i<k;i++){
            System.out.print(a[i]);
            }
        }
    }